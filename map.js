function button1(){ 
    var T = document.getElementById("result"),
    displayValue = "";
    if (T.style.display == "")
    T.style.display = "none";

    var currentDate = new Date()
    var TimeMins = currentDate.getMinutes()
    var TimeHour = currentDate.getHours()

    if (TimeHour > 12) {
        TimeHour=(TimeHour - 12);}

    TimeHour = TimeHour - 9;

    if (TimeHour > 12) {
        TimeHour=(TimeHour - 12);
        if (TimeMins < 10) {
            TimeMins = "0" +TimeMins;
        }
        TimeMins = TimeMins+ " PM";
    }

    else if (TimeHour < 0) {TimeHour = TimeHour +12;
        if (TimeMins < 10) {
            TimeMins = "0" +TimeMins;
        }
        TimeMins = TimeMins + " AM";}

    else {
        if (TimeMins < 10) {
            TimeMins = "0" +TimeMins;
        }
        TimeMins = TimeMins + " AM";}


 T.style.display = displayValue;
        document.getElementById('resultTime').innerText = (TimeHour + ":" + TimeMins);
    
} 

